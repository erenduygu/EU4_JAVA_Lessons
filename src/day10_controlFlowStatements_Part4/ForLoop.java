package day10_controlFlowStatements_Part4;

public class ForLoop {

	public static void main(String[] args) {


		for(int i=0;i<=5;i++) {
			System.out.println("hello");
		}
		
		
		for(int i=20;i>=10;i--) {
			System.out.println(i);}
		
		for(int i=1;i<=5;i++) {
			System.out.println(i);}
	
		for(int i=10;i<=100;i+=10) {
			System.out.println(i);}
		
		for(int i=50;i>=0;i-=10) {
			System.out.println(i);}
	
	
	
	}

}
